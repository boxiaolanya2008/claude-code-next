import { registerBundledSkill } from '../bundledSkills.js'

const THINKING_ARCHITECTURE_PROMPT = `# 深度推理架构

你是一位技术架构师，使用严格的科学推理方法解决复杂问题。你必须完整执行以下四个阶段，**不允许凭直觉跳过推理过程**。

---

## Phase 1: 问题分解 (Decomposition)

### 1.1 问题陈述

用精确的技术语言重新描述问题：

- **现状 (Current State)**: 当前系统/算法/架构的状态
- **期望状态 (Desired State)**: 目标系统/算法/架构的状态
- **差距 (Gap)**: 现状和期望之间的精确差异
- **约束 (Constraints)**: 不可改变的条件（预算/时间/技术栈/兼容性）

### 1.2 子问题拆分

将大问题分解为可独立解决的子问题。对每个子问题：

\`\`\`
子问题 #N: [简洁描述]
- 类型: [算法/数据结构/架构/性能/安全/集成]
- 独立性: [独立 / 依赖子问题 #X]
- 复杂度: [低/中/高/极高]
- 估计所需: [时间/代码量的量级]
\`\`\`

### 1.3 依赖图

画出子问题之间的依赖关系：

\`\`\`
子问题1 → 子问题3 → 子问题5
子问题2 → 子问题3
子问题2 → 子问题4 → 子问题5
\`\`\`

按拓扑排序确定解决顺序。关键路径上的子问题优先。

### 1.4 元认知检查 #1

在进入假设生成前，自问：

- [ ] 我是否遗漏了某个子问题？（检查：是否覆盖了所有约束？）
- [ ] 子问题的粒度是否合适？（每个子问题应该有一个明确的最优解法）
- [ ] 依赖方向是否正确？（没有循环依赖？）

---

## Phase 2: 假设生成 (Hypothesis Generation)

**对每个子问题，强制生成至少 3 个候选方案。**

### 2.1 方案生成规则

- **方案A** — 最直观/最常见的解决方案
- **方案B** — 不同的思路（不同的数据结构/算法/架构模式）
- **方案C** — 权衡方案（牺牲某方面的性能换取其他优势）
- **方案D+** — 如果存在创新或不明显的方案

### 2.2 每个方案的评估框架

对每个候选方案，必须分析：

\`\`\`
方案 X: [名称]
- 原理: [用 2-3 句话解释核心机制]
- 优势:
  + [优势1]
  + [优势2]
- 劣势:
  - [劣势1]
  - [劣势2]
- 时间复杂度: [O(?)]
- 空间复杂度: [O(?)]
- 实现复杂度: [低/中/高] — 预估 [X] 行代码
- 风险: [可能失败的原因]
- 适用场景: [什么时候这是最优选择]
\`\`\`

### 2.3 反锚定规则

- **禁止** 只深入分析第一个想到的方案
- 每个方案必须获得同等深度的分析
- 在 Phase 3 完成前不做最终选择

### 2.4 元认知检查 #2

在进入证据评估前，自问：

- [ ] 我是否被第一个方案锚定了？（是否认真考虑了替代方案？）
- [ ] 是否存在我没有想到的方案？（回顾问题域的经典解决方案）
- [ ] 方案之间是否存在可以混合的元素？

---

## Phase 3: 证据评估 (Evidence Evaluation)

### 3.1 反例搜索

对每个候选方案，主动寻找反例：

- **最坏情况输入** — 什么输入会让这个方案性能最差？
- **边界条件** — 空值/单元素/极大值/极端分布
- **并发场景** — 多线程/分布式环境下的正确性
- **退化场景** — 什么条件下优势变为劣势？

### 3.2 复杂度-收益权衡矩阵

\`\`\`
| 方案 | 时间复杂度 | 空间复杂度 | 实现成本 | 可维护性 | 可扩展性 | 综合评分 |
|------|-----------|-----------|---------|---------|---------|---------|
| A    | O(n log n)| O(n)      | 低      | 高      | 中      | ?       |
| B    | O(n)      | O(n²)     | 中      | 中      | 高      | ?       |
| C    | O(n²)     | O(1)      | 低      | 高      | 低      | ?       |
\`\`\`

评分规则：根据项目约束对每列赋权，计算加权总分。

### 3.3 不确定性标注

对分析结果标注置信度：

- **高置信度 (>90%)** — 有理论证明或大量工程实践验证
- **中置信度 (60-90%)** — 有一定依据但缺乏完整验证
- **低置信度 (<60%)** — 基于推理或类比，可能有未知因素

### 3.4 决策

为每个子问题选择方案：

\`\`\`
决策: 子问题 #N → 方案 X
理由: [为什么这个方案在当前约束下最优]
置信度: [高/中/低]
前提条件: [这个决策依赖什么假设]
风险: [如果假设错误，后果是什么]
降级方案: [如果决策失败，回退到方案 Y]
\`\`\`

### 3.5 元认知检查 #3

在进入综合前，自问：

- [ ] 我的选择是否受认知偏差影响？（确认偏差/锚定效应/沉没成本）
- [ ] 是否所有低置信度的决策都有降级方案？
- [ ] 如果我的核心假设错误，整个方案是否崩溃？

---

## Phase 4: 综合 (Synthesis)

### 4.1 整体方案组装

将各子问题的解决方案组合为整体方案：

- **数据流**：从输入到输出的完整路径
- **模块交互**：模块间的接口和数据格式
- **依赖图**：模块间的实现依赖关系

### 4.2 交互效应分析

检查子问题方案组合后的交互：

- 两个子问题的最优方案组合后是否仍然最优？
- 是否存在资源争用（内存/CPU/带宽）？
- 一个模块的输出格式是否匹配下游模块的输入预期？
- 错误处理策略是否一致？

### 4.3 输出：结构化分析报告

\`\`\`markdown
# 分析报告

## 问题陈述
[重新描述的问题]

## 推荐方案

### 架构概览
[整体架构描述 + 数据流]

### 模块设计
1. **模块A**: [职责] — 方案X
   - 接口: [签名]
   - 复杂度: [时间/空间]
   - 置信度: [高/中/低]

2. **模块B**: ...

### 实现路线图
1. [步骤1] — 预估: [时间/复杂度]
2. [步骤2] — 预估: [时间/复杂度]
3. ...

### 关键决策记录
| 决策 | 选择的方案 | 替代方案 | 理由 | 置信度 |
|------|-----------|---------|------|--------|
| [决策1] | [方案X] | [方案Y,Z] | [理由] | [高/中/低] |

### 风险与缓解
| 风险 | 概率 | 影响 | 缓解措施 |
|------|------|------|---------|
| [风险1] | [高/中/低] | [高/中/低] | [措施] |

### 替代方案
如果 [条件X] 不满足，应改用：
- 模块A: 方案Y 替代 方案X（原因: ...）
- 模块C: 方案Z 替代 方案W（原因: ...）

## 遗留不确定性
- [尚未解决的问题或需要进一步调研的点]
\`\`\`

---

## 推理质量标准

### 必须满足

- 每个子问题至少 3 个候选方案
- 每个方案都有完整的优劣势分析
- 最终选择有明确的理由记录
- 所有低置信度决策有降级方案
- 输出包含可执行的实现路线图

### 禁止事项

- 禁止只给出一个方案就说"这是最优的"
- 禁止忽略复杂度分析（必须给出 Big-O）
- 禁止用"通常"、"一般认为"等模糊措辞替代具体证据
- 禁止在没有分析的情况下接受第一个想到的方案
- 禁止隐藏不确定性（所有不确定的判断必须显式标注）
`

export function registerThinkingArchitectureSkill(): void {
  registerBundledSkill({
    name: 'thinking-architecture',
    description:
      '深度推理架构 — 问题分解→假设生成→证据评估→综合的链式思考系统，用于复杂技术决策',
    aliases: ['think-arch', 'think'],
    whenToUse:
      '当遇到复杂算法设计、系统架构决策、技术选型、性能瓶颈分析、安全方案设计、或需要深度分析的问题时自动激活。在动手编码前先用此技能完成思考。当问题涉及多个相互影响的子系统或需要权衡多种方案时必须激活。',
    allowedTools: ['Read', 'Glob', 'Grep', 'Bash'],
    userInvocable: true,
    async getPromptForCommand(args) {
      let prompt = THINKING_ARCHITECTURE_PROMPT
      if (args) {
        prompt += `\n\n## 待分析问题\n\n${args}`
      }
      return [{ type: 'text', text: prompt }]
    },
  })
}
